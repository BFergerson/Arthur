function andOperator() {
    if (true && true) {
        console.log("");
    }
}

function orOperator() {
    if (true || true) {
        console.log("");
    }
}

function ternaryOperator() {
    var a, b;
    a = 10;
    b = (a == 1) ? 20 : 30;
}

