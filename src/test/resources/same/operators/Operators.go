package main

import "fmt"

func andOperator() {
   if (true && true) {
      fmt.Println("")
   }
}

func orOperator() {
   if (true || true) {
      fmt.Println("")
   }
}

func equalOperator() {
    if (true == true) {
        fmt.Println("")
    }
}

func notEqualOperator() {
    if (true != true) {
        fmt.Println("")
    }
}