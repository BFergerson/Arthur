public class Conditionals {

    public void ifConditional() {
        if (true) {
            System.out.println("");
        }
    }

    public void ifElseConditional() {
        if (true) {
            System.out.println("");
        } else {
            System.out.println("");
        }
    }

    public void ifElseIfConditional() {
        if (true) {
            System.out.println("");
        } else if (true) {
            System.out.println("");
        }
    }
}