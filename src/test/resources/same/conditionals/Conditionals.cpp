#include <iostream>

void ifConditional() {
    if (true) {
        std::cout << "";
    }
}

void ifElseConditional() {
    if (true) {
        std::cout << "";
    } else {
        std::cout << "";
    }
}

void ifElseIfConditional() {
    if (true) {
        std::cout << "";
    } else if (true) {
         std::cout << "";
    }
}

void switchConditional() {
    switch (1) {
    }
}

void switchCaseConditional() {
    switch (1) {
        case 1:
            std::cout << "";
    }
}