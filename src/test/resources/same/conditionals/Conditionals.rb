def ifConditional
  if true
    puts ""
  end
end

def unlessConditional
  unless false
    puts ""
  end
end

def ifElseConditional
  if true
    puts ""
  else
    puts ""
  end
end

def ifElseIfConditional
  if true
    puts ""
  elsif true
    puts ""
  end
end

def switchCaseConditional
  case 1
  when 1
    puts ""
  else
    puts ""
  end
end