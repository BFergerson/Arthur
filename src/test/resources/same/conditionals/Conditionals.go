package main

import "fmt"

func ifConditional() {
   if true {
      fmt.Println("")
   }
}

func ifElseConditional() {
   if true {
      fmt.Println("")
   } else {
      fmt.Println("")
   }
}

func ifElseIfConditional() {
   if true {
      fmt.Println("")
   } else if true {
      fmt.Println("")
   }
}

func switchConditional() {
    switch 1 {
    }
}

func switchCaseConditional() {
    switch 1 {
        case 1:
            fmt.Println("")
    }
}