using System;

public class Conditionals
{

    public void ifConditional()
    {
        if (true)
        {
            Console.WriteLine("");
        }
    }

    public void ifElseConditional()
    {
        if (true)
        {
            Console.WriteLine("");
        }
        else
        {
            Console.WriteLine("");
        }
    }

    public void ifElseIfConditional()
    {
        if (true)
        {
            Console.WriteLine("");
        }
        else if (true)
        {
            Console.WriteLine("");
        }
    }

    public void switchConditional()
    {
        switch (1)
        {
        }
    }

    public void switchCaseConditional()
    {
        switch (1)
        {
            case 1:
                Console.WriteLine("1");
                break;
        }
    }
}