function ifConditional() {
    if (true) {
        console.log("");
    }
}

function ifElseConditional() {
    if (true) {
        console.log("");
    } else {
        console.log("");
    }
}

function ifElseIfConditional() {
    if (true) {
        console.log("");
    } else if (true) {
        console.log("");
    }
}