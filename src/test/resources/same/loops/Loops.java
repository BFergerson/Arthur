public class Loops {
    public void forLoop() {
        for (int i = 0; i < 10; i++) {
        }
    }

    public void forEachLoop() {
        int[] nums = {0, 1};
        for (int num : nums) {
        }
    }

    public void whileLoop() {
        while (true) {
        }
    }

    public void doWhileLoop() {
        do {
        } while (true);
    }
}